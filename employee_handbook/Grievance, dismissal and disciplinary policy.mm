<node CREATED="1314305274997" ID="ID_1600459565" MODIFIED="1314306171809">
<richcontent TYPE="NODE"><html>
  <head>
    
  </head>
  <body>
    <p>
      Failure to improve performance in response to the procedure so far, a repeat of misconduct for which a warning has previously been issued, or a first instance of serious misconduct or serious poor performance, will result in a Final Written Warning being issued &#8211; but only after the employee has been given the chance to present their case at a meeting. The Final Written Warning will give details of, and grounds for, the complaint, how he or she needs to improve their conduct or performance, the timescale over which the improvement is to be achieved and warn that dismissal will probably result if the terms of the warning are not complied with.
    </p>
    <p>
      <br />
      
    </p>
    <p>
      Warnings will not normally remain in force indefinitely but will be disregarded for disciplinary purposes after a specified period of satisfactory conduct. The time limit set will depend on the nature and gravity of the offence and on the employee's past disciplinary record. Warnings will normally remain on file for 6 months for a Written Warning and 12 months for a Final Written Warning. However, if the misconduct is particularly serious, a Final Written Warning will not be removed from the record.
    </p>
