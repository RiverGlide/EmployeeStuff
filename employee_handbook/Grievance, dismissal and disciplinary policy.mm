  </head>
  <body>
    <p>
      <font face="SansSerif" size="3">If an employee is subject to disciplinary action:<br size="3" face="SansSerif" /></font>
    </p>
    <ul>
      <li>
        <p>
          <font size="3" face="SansSerif">The procedure is designed to establish the facts quickly and to deal consistently with disciplinary issues. No disciplinary action will be taken until the matter has been fully investigated. </font>
        </p>
      </li>
      <li>
        <p>
          <font size="3" face="SansSerif">At every stage the employee will be advised in writing of the nature of the complaint. </font>
        </p>
